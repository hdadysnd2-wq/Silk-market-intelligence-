def _f(value, source, conf, note):
    return {"value": value, "source": source, "confidence": conf,
            "note": note, "retrieved_at": "2026-07-20", "status": ""}


# سرد بالسجل الأكاديمي — نفس حقائق المدوّنة القانونية حرفياً (9م$، 5%،
# HHI 2900، 6$/كجم مقابل 0.67$/كجم، القيد التصنيفي)، بالصياغة التي ينتجها
# الكاتب مع عقد ACADEMIC_WRITER_CONTRACT (style=academic). عيّنة موسومة —
# تمثّل حالة ما بعد إعادة التوليد الأكاديمية على النشر الحي.
